export function getTherapistById(id: number): Therapist | undefined {
  return therapists.find((t) => t.id === id);
}

export function getFeaturedTherapists(count: number = 2): Therapist[] {
  return therapists.slice(0, count);
}

export function getMatchedTherapists(): Therapist[] {
  return [therapists[0], therapists[1], therapists[6]];
}

export function filterTherapistsBySpecialty(specialty: string): Therapist[] {
  if (!specialty) return therapists;
  return therapists.filter(t => t.specialty === specialty);
}

export function filterTherapistsByAgeGroup(ageGroup: "children" | "teenagers" | "adults"): Therapist[] {
  return therapists.filter(t => t.ageGroups?.includes(ageGroup));
}
